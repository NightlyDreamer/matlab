function dy = tbp(t,y)
G = 6.67*10^(-11);
dy = zeros(21,1);
L12 = sqrt((y(1)-y(8))^2 + (y(2)-y(9))^2 + (y(3)-y(10))^2)^3; 
L23 = sqrt((y(8)-y(15))^2 + (y(9)-y(16))^2 + (y(10)-y(17))^2)^3; 
L13 = sqrt((y(1)-y(15))^2 + (y(2)-y(16))^2 + (y(3)-y(17))^2)^3;
dy(7)  = y(7);
dy(14) = y(14);
dy(21) = y(21);
dy(1)  = y(4);
dy(2)  = y(5);
dy(3)  = y(6);
dy(8)  = y(11);
dy(9)  = y(12);
dy(10) = y(13);
dy(15) = y(18);
dy(16) = y(19);
dy(17) = y(20);
dy(4) = -G*y(14)*(y(1)-y(8))/L12-G*y(21)*(y(1)-y(15))/L13;
dy(11) = -G*y(21)*(y(8)-y(15))/L23-G*y(7)*(y(8)-y(1))/L12;
dy(18) = -G*y(7)*(y(15)-y(1))/L13-G*y(14)*(y(15)-y(8))/L23;
dy(5) = -G*y(14)*(y(2)-y(9))/L12-G*y(21)*(y(2)-y(16))/L13;
dy(12) = -G*y(21)*(y(9)-y(16))/L23-G*y(7)*(y(9)-y(2))/L12;
dy(19) = -G*y(7)*(y(16)-y(2))/L13-G*y(14)*(y(16)-y(9))/L23;
dy(6) = -G*y(14)*(y(3)-y(10))/L12-G*y(21)*(y(3)-y(17))/L13;
dy(13) = -G*y(21)*(y(10)-y(17))/L23-G*y(7)*(y(10)-y(3))/L12;
dy(20) = -G*y(7)*(y(17)-y(3))/L13-G*y(14)*(y(17)-y(10))/L23;
